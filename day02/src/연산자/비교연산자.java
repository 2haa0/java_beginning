package 연산자;

public class 비교연산자 {

	public static void main(String[] args) {
		//비교연산자: ==, !=, >(초과), >=(이상), <(미만), =<(이하)
		//비교결과가 중요(논리데이터boolean으로 출력되어 true 혹은 false)_ 제어문에서 역할 큼  
		
		int x = 100;
		int y = 200;
		
		System.out.println(x == y);
		System.out.println(x != y);
		System.out.println(x >= y);
		System.out.println(x > y);
		System.out.println(x < y);
		
	}

}
